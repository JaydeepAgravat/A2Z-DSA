# 15 data types

# int float complex
x = 10 or -10.5 or complex(2, 3)

# string list tuple range
x = 'abc' or [1, 2, 3] or (1, 2, 3) or range(3)

# set frozenset
x = {1, 2, 3} or frozenset([1, 2, 3])

# dictionary
x = {1: 'a', '1': 2}

# boolean
x = True or False

# none
x = None

# bytes bytearray memoryview
x = b'bytes'
x = bytearray(1)
x = memoryview(x)
